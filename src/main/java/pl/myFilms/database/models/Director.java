package pl.myFilms.database.models;

import com.j256.ormlite.dao.ForeignCollection;
import com.j256.ormlite.field.DatabaseField;
import com.j256.ormlite.field.ForeignCollectionField;
import com.j256.ormlite.table.DatabaseTable;

@DatabaseTable(tableName = "DIRECTOR")
public class Director implements BaseModel {

    public Director() {
    }

    @DatabaseField(generatedId = true)
    private int id;

    @DatabaseField(columnName = "NAME", canBeNull = false, unique = true)
    private String name;

    @DatabaseField(columnName = "SURNAME", canBeNull = false, unique = true)
    private String surname;

    @ForeignCollectionField(eager = true)
    private ForeignCollection<Film> films;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public ForeignCollection<Film> getFilms() {
        return films;
    }

    public void setFilms(ForeignCollection<Film> films) {
        this.films = films;
    }
}
